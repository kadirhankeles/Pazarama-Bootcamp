import Foundation

class MathematicFunctions {
    
    func addIntegers(x: Int, y: Int) -> Int {
        return x+y
    }
    
    func multiplyIntegers(x: Int, y: Int) -> Int {
        return x*y
    }
    
    func decutIntegers(x: Int, y: Int) -> Int {
        return x-y
    }
    
}
